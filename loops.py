# While loop

# Increment
number = 5
while number <= 10:
    print(number)
    number += 1

# Decrement
number = 30
while number >= 25:
    print(number)
    number -= 1

# For loop
for x in range(105,110,2):
    print("Number is",x)

for x in range(30,24,-1):
    print(x)

devices = ["laptop", "mouse", "computer"]
for y in devices:
    print(y)